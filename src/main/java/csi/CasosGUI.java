    public void setCasos(ArrayList<String> casos) {
        // limpa todos os possiveis itens antigos
        listCasos.removeAll();
        
        if (casos == null) return;
        
        DefaultListModel listModel = new DefaultListModel();  
        Iterator i = casos.iterator();
        
        while (i.hasNext()) {
            listModel.addElement((String) i.next());
        }
        
        listCasos.setModel(listModel);
    }

