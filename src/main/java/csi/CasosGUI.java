private void listCasosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listCasosMouseClicked
    if ((evt.getClickCount() >= 2) && (listCasos.getSelectedIndex() >= 0)) {
        String caso = (String) listCasos.getModel().getElementAt( listCasos.getSelectedIndex() );
        String[] registro = caso.split("-");
        Controller.getController().carregaCaso( Integer.parseInt(registro[0].trim().substring(1)) );
    }
}//GEN-LAST:event_listCasosMouseClicked

